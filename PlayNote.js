
//By using the command Play(tone) the corresponding frequency will be played. For example, "Play(5);" will play an F.


function Play (tone){
	
	
		if(tone == 0){
		C();
		}
		if(tone == 1){
		Db();
		}
		if(tone == 2){
		D();
		}
		if(tone == 3){
		Eb();
		}
		if(tone == 4){
		E();
		}
		if(tone == 5){
		F();
		}
		if(tone == 6){
		Gb();
		}
		if(tone == 7){
		G();
		}
		if(tone == 8){
		Ab();
		}
		if(tone == 9){
		A();
		}
		if(tone == 10){
		Bb();
		}
		if(tone == 11){
		B();
		}
		if(tone == 12){
		C2();
		}
		if(tone == 13){
		Db2();
		}
		if(tone == 14){
		D2();
		}
		if(tone == 15){
		Eb2();
		}
		if(tone == 16){
		E2();
		}
		if(tone == 17){
		F2();
		}
		if(tone == 18){
		Gb2();
		}
		if(tone == 19){
		G2();
		}
		if(tone == 20){
		Ab2();
		}
		if(tone == 21){
		A2();
		}
		if(tone == 22){
		Bb2();
		}
		if(tone == 23){
		B2();
		}
		if(tone == 24){
		C3();
		}
}


