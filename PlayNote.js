    261.6,	           //frequency
    attack,                //attack
    decay,              //decay
    waveform,       //waveform
    volume,                //Volume
    pan,             //pan
    wait,                //wait before playing
    pitchbend,             //pitch bend amount
    reversebend,            //reverse bend
    pitchrange,                //random pitch range
    dissonance,               //dissonance
